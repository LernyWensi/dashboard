const projects = document.querySelector('#projects');
const announcements = document.querySelector('#announcements');
const trends = document.querySelector('#trending');

function getProjectHtml(title, description) {
    return `
        <a href="#">
            <h3>${title}</h3>
            <p>${description}</p>
            <menu>
                <li>
                    <button type="button">
                        <span class="material-symbols-rounded">star</span>
                    </button>
                </li>
                <li>
                    <button type="button">
                        <span class="material-symbols-rounded">visibility</span>
                    </button>
                </li>
                <li>
                    <button type="button">
                        <span class="material-symbols-rounded">share</span>
                    </button>
                </li>
            </menu>
        </a>
    `;
}

function getAnnouncementHtml(title, description) {
    return `
        <a href="#">
            <h3>${title}</h3>
            <p>${description}</p>
        </a>
    `;
}

function getTrendHtml(avatar, usertag, project) {
    return `
        <a class="profile" href="#">
            <img class="avatar" src="${avatar}" alt="Avatar" />
            <section>
                <p class="usertag">@${usertag}</p>
                <p class="project">${project}</p>
            </section>
        </a>
    `;
}

function createItemFromHtml(html) {
    const item = document.createElement('li');
    item.innerHTML = html;
    return item;
}

PROJECTS.forEach(({ title, description }) => {
    const project = createItemFromHtml(getProjectHtml(title, description));
    projects.querySelector('ul').appendChild(project);
});

ANNOUNCEMENTS.forEach(({ title, description }) => {
    const announcement = createItemFromHtml(getAnnouncementHtml(title, description));
    announcements.querySelector('ul').appendChild(announcement);
});

TRENDS.forEach(({ avatar, usertag, project }) => {
    const trend = createItemFromHtml(getTrendHtml(avatar, usertag, project));
    trends.querySelector('ul').appendChild(trend);
});
